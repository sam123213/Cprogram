//check vowel and consonant

#include<stdio.h>
void main()
{
    char c;
    printf("Enter the character:");
    scanf("%c",&c);
    if(c=='a','A'||c=='e',"E"||c=='i','I'||c=='o','O'||c=='u','U')
    {
        printf("It is Vowel")  ;

    }
    else
    {
        printf("It is Consonant");
    }
    
}